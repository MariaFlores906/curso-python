#Escribir un programa que solicite al usuario una letra y, si es una vocal, 
  # muestre el mensaje “es vocal”.

Vocales = ['a', 'e', 'i', 'o', 'u']

Letra = input('Ingrese una letra: ')

if Letra == Vocales[0]:
    print('Es vocal')

elif Letra == Vocales[1]:
    print('Es vocal')

elif Letra == Vocales[2]:
    print('Es vocal')

elif Letra == Vocales[3]:
    print('Es vocal')

elif Letra == Vocales[4]:
    print('Es vocal')

else:
    print('No es una vocal')
