# Imprimir todos los numeros, del 0 al 9, utilizando una repetición for.
# Imprimir todos los números entre el 100 y el 199.
# Imprimir los números entre el 5 y el 20, saltando de tres en tres.


for num in range(9):
    print(num)

for num in range(100, 199):
    print(num)

for num in range(5, 20, 3):
    print(num)

